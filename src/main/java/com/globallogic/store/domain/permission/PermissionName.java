/**
 * Permission names that can be used
 *
 * @author oleksii.slavik
 */
public enum PermissionName {

    /**
     * read permission
     */
    READ,

    /**
     * create permission
     */
    CREATE,

    /**
     * update permission
     */
    UPDATE,

    /**
     * delete permission
     */
    DELETE,

    /**
     * administration permission
     */
    ADMINISTRATION
}
